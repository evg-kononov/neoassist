def precision(tp: int, fp: int) -> float:
    """Вычисляет точность (precision).

    Precision показывает, какая доля положительных предсказаний модели оказалась верной.
    Добавлено слагаемое `1e-10` в знаменатель для избежания деления на ноль.

    Args:
        tp: Количество истинно положительных результатов (True Positives).
        fp: Количество ложно положительных результатов (False Positives).

    Returns:
        Значение точности в диапазоне [0, 1].
    """
    return tp / (tp + fp + 1e-10)


def recall(tp: int, fn: int) -> float:
    """Вычисляет полноту (recall).

    Recall показывает, какую долю всех положительных случаев модель смогла найти.
    Добавлено слагаемое `1e-10` в знаменатель для избежания деления на ноль.

    Args:
        tp: Количество истинно положительных результатов (True Positives).
        fn: Количество ложно отрицательных результатов (False Negatives).

    Returns:
        Значение полноты в диапазоне [0, 1].
    """
    return tp / (tp + fn + 1e-10)
